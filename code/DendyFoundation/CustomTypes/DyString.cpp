         dyString& dyString::createFormatedString(const char* a_string, ...) {
         DENDYENGINE_CALLSTACK_ENTER;
            char tmpBuffer[512];
            va_list args;
            va_start(args, a_string);
            vsprintf_s(tmpBuffer, a_string, args);
            va_end(args);
            dyString newString(tmpBuffer);
         DENDYENGINE_CALLSTACK_EXIT;
            return newString;
         }


