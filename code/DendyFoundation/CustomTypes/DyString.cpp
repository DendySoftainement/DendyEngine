   const char* dyString::allocConstCharFancyPanel( dyString a_string ) {
      dyString fancySign( "/////////////////////////////////////////////////////////////////////////////////////\n////" );
      //dyString fancySign( "-------------------------------------------------------------------------------------\n----" );
      dyUInt64 spacesToInsert = 78 - a_string.len( );
      for ( dyUInt16 i = 0; i < spacesToInsert / 2; i++ ) {
         fancySign += " ";
      }
      fancySign += a_string;
      for ( dyUInt16 i = 0; i < spacesToInsert / 2; i++ ) {
         fancySign += " ";
      }
      fancySign += "////\n/////////////////////////////////////////////////////////////////////////////////////\n";
      //fancySign += "----\n---------------------------------------------------------------------------------------\n";
      return fancySign.asAllocatedConstChar( );
   }

